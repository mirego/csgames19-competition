package ServeResponse;

public class Review {
	public Integer taste = null;
	public Integer volume = null;
	public Integer strength = null;
	
	
	//getter and setters
	public Integer getTaste() {
		return taste;
	}
	public Integer getVolume() {
		return volume;
	}
	public Integer getStrength() {
		return strength;
	}
	public void setTaste(Integer taste) {
		this.taste = taste;
	}
	public void setVolume(Integer volume) {
		this.volume = volume;
	}
	public void setStrength(Integer strength) {
		this.strength = strength;
	}
}
